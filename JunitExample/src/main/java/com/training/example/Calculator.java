package com.training.example;

public class Calculator {
	public int sum(int x, int y) {
		return x+y;
		
	}
	
	public int sub(int x, int y) {
		return x-y;
		
	}
	
	public int mul(int x, int y) {
		return x*y;
		
	}
	
	public int div(int x, int y) {
		return x/y;
		
	}
	
	public double avg(double x, double y, double z) {
		double res = x+y+z;
		return res/3;
	}
	
	
}
